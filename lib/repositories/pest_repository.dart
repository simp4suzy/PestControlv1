  // Methods to interact with the data
  List<Category> getAllCategories() => List.unmodifiable(_categories);

  List<Pest> getAllPests() => List.unmodifiable(_pests);

  List<Pest> getPestsByCategory(String categoryId) => 
      _pests.where((pest) => pest.category == categoryId).toList();

  List<Pest> searchPests(String query) {
    if (query.isEmpty) return [];
    
    final queryLower = query.toLowerCase();
    return _pests.where((pest) {
      // Check name words
      final nameWords = pest.name.toLowerCase().split(' ');
      final hasNameMatch = nameWords.any((word) => 
          word.startsWith(queryLower) || pest.name.toLowerCase().contains(queryLower));
      
      // Check scientific name words
      final sciNameWords = pest.scientificName.toLowerCase().split(' ');
      final hasSciNameMatch = sciNameWords.any((word) => 
          word.startsWith(queryLower) || pest.scientificName.toLowerCase().contains(queryLower));
      
      // Check description
      final hasDescriptionMatch = pest.description.toLowerCase().contains(queryLower);
      
      return hasNameMatch || hasSciNameMatch || hasDescriptionMatch;
    }).toList();
  }

  Pest? getPestById(String id) {
    try {
      return _pests.firstWhere((pest) => pest.id == id);
    } catch (e) {
      return null;
    }
  }

  // Helper method to get category name by ID
  String getCategoryName(String categoryId) {
    return _categories.firstWhere(
      (category) => category.id == categoryId,
      orElse: () => Category(id: '', name: 'Unknown', description: '', iconPath: ''),
    ).name;
  }
